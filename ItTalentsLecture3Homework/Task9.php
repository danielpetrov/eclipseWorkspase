<?php
/* 9:Да се прочетат 2 числа от клавиатурата А и В.
Да се изведат всички числа от А до В на степен 2(разделени с
запетая).Ако някое число е кратно на 3, да се изведе съобщение че
числото се пропуска „skip 3“.Ако сумата от всички изведени числа (без
пропуснатите) стане по-голяма от 200, да се прекрати извеждането. */

require_once 'Readline.php';

$firstNumber = ( int ) readline ( "Enter firstNumber:" . PHP_EOL );

$lastNumber = ( int ) readline ( "Enter lastNumber:" . PHP_EOL );

$sumOfAllPowers = 0;

for ($i = $firstNumber; $i < $lastNumber; $i++) {
	$currentPower = $i * $i;
	
	if (!($currentPower % 3)) {
		echo "skip " . $i . " ";;
	} else {
		echo $currentPower . " ";
		
		$sumOfAllPowers += $currentPower;
		
		if ($sumOfAllPowers > 200) {
			break;
		}
	}
	
}